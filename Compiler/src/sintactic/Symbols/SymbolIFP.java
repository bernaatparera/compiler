package sintactic.Symbols;

public class SymbolIFP {
    private int end;
    private String line;
    public SymbolIFP(int end, String line) {
        this.end = end;
        this.line = line;
    }
    public int getEnd() {
        return end;
    }
    public String getLine() {
        return line;
    }
}

