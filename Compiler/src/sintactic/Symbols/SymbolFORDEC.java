package sintactic.Symbols;

public class SymbolFORDEC {
    private int pre;
    private int end;
    public SymbolFORDEC(int pre, int end) {
        this.pre = pre;
        this.end = end;
    }

    public int getPre() {
        return pre;
    }

    public int getEnd() {
        return end;
    }
}
